# Exercício - Crie um programa que receba a distância de uma cidade em quilômetros e a transforme em
# metros, mostrando o resultado na tela.

distanciaCidade = int(input("Digite a distância de uma cidade: " + "km"))
converteKm = distanciaCidade * 1000
print("A distância informada em metros é de: ", converteKm, "m")
